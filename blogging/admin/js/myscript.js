
function Sidebar_Blog() {
  if(document.getElementById("Sidebar_Blog").style.display == "none"){
    document.getElementById("Sidebar_Blog").style.display = "block";
  }else{
    document.getElementById("Sidebar_Blog").style.display = "none";
  }
  
}

